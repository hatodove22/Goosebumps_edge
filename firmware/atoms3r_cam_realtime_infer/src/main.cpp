// -------------------- Helpers --------------------
static inline float clampf(float x, float lo, float hi) {
  if (x < lo) return lo;
  if (x > hi) return hi;
  return x;
}

static inline float sigmoidf_clamped(float z) {
  // expf overflows around ~88, but we clamp much tighter for numerical stability.
  z = clampf(z, -15.0f, 15.0f);
  return 1.0f / (1.0f + expf(-z));
}

static inline uint8_t rgb565_to_gray(uint16_t p) {
  // RGB565 -> approximate luma (0..255)
  // Expand to 8-bit first.
  const uint8_t r8 = (uint8_t)(((p >> 11) & 0x1F) << 3);
  const uint8_t g8 = (uint8_t)(((p >> 5) & 0x3F) << 2);
  const uint8_t b8 = (uint8_t)((p & 0x1F) << 3);
  // ITU-R BT.601 luma approx: 0.299R + 0.587G + 0.114B
  // integer: (77R + 150G + 29B) >> 8
  const uint16_t y = (uint16_t)(r8 * 77u + g8 * 150u + b8 * 29u) >> 8;
  return (uint8_t)y;
}

static void camera_power_enable() {
  // AtomS3R-CAM uses POWER_N (GPIO18) active-low enable.
  // We intentionally "power-cycle" the camera enable pin to avoid rare
  // probe failures at boot (ESP_ERR_NOT_FOUND).
  pinMode(CAM_PIN_POWER_N, OUTPUT);
  digitalWrite(CAM_PIN_POWER_N, HIGH);  // disable
  delay(CAM_POWER_OFF_DELAY_MS);
  digitalWrite(CAM_PIN_POWER_N, LOW);   // enable
  delay(CAM_POWER_ON_DELAY_MS);
}

static void led_init() {
  ledcSetup(LEDC_CH, LED_PWM_FREQ_HZ, LED_PWM_RES_BITS);
  ledcAttachPin(LED_PWM_PIN, LEDC_CH);
  ledcWrite(LEDC_CH, g_led_pwm);
}

static void led_set(uint8_t pwm) {
  g_led_pwm = pwm;
  ledcWrite(LEDC_CH, g_led_pwm);
}

static bool wifi_connect() {
  WiFi.mode(WIFI_STA);
  WiFi.setSleep(false);
  WiFi.begin(WIFI_SSID, WIFI_PASS);

  Serial.printf("[WiFi] connecting to %s\n", WIFI_SSID);
  const uint32_t t0 = millis();
  while (WiFi.status() != WL_CONNECTED) {
    delay(300);
    Serial.print(".");
    if (millis() - t0 > 20000) {
      Serial.println("\n[WiFi] timeout");
      return false;
    }
  }
  Serial.printf("\n[WiFi] connected. IP=%s RSSI=%d\n", WiFi.localIP().toString().c_str(), WiFi.RSSI());
  return true;
}

