static camera_config_t make_camera_config() {
  camera_power_enable();

  camera_config_t config;
  config.ledc_channel = LEDC_CHANNEL_1;
  config.ledc_timer   = LEDC_TIMER_1;

  config.pin_d0       = CAM_PIN_D0;
  config.pin_d1       = CAM_PIN_D1;
  config.pin_d2       = CAM_PIN_D2;
  config.pin_d3       = CAM_PIN_D3;
  config.pin_d4       = CAM_PIN_D4;
  config.pin_d5       = CAM_PIN_D5;
  config.pin_d6       = CAM_PIN_D6;
  config.pin_d7       = CAM_PIN_D7;
  config.pin_xclk     = CAM_PIN_XCLK;
  config.pin_pclk     = CAM_PIN_PCLK;
  config.pin_vsync    = CAM_PIN_VSYNC;
  config.pin_href     = CAM_PIN_HREF;
  config.pin_sccb_sda = CAM_PIN_SIOD;
  config.pin_sccb_scl = CAM_PIN_SIOC;

  // IMPORTANT:
  // - esp_camera treats pin_pwdn as active-high power-down.
  // - AtomS3R-CAM GPIO18 is POWER_N (active-low enable).
  // So we control POWER_N manually and keep pin_pwdn unused.
  config.pin_pwdn     = -1;
  config.pin_reset    = CAM_PIN_RESET;

  // Put camera SCCB on I2C port 1.
  config.sccb_i2c_port = 1;

  // M5Stack docs recommend XCLK 20MHz for AtomS3R-CAM.
  config.xclk_freq_hz = 20000000;

  // GC0308 does not output JPEG natively. Capture RGB565.
  config.pixel_format = PIXFORMAT_RGB565;

  config.frame_size   = (framesize_t)g_frame_size;
  config.jpeg_quality = g_jpeg_quality;
  config.fb_count     = 2;
  config.fb_location  = CAMERA_FB_IN_PSRAM;
  config.grab_mode    = CAMERA_GRAB_LATEST;

  return config;
}

static bool camera_init() {
  if (!psramFound()) {
    Serial.println("[CAM] PSRAM not found. This board should have 8MB PSRAM.");
  } else {
    Serial.printf("[CAM] PSRAM OK. size=%u\n", ESP.getPsramSize());
  }

  camera_config_t config = make_camera_config();
  esp_err_t err = esp_camera_init(&config);
  if (err != ESP_OK) {
    Serial.printf("[CAM] init failed 0x%x\n", (int)err);
    return false;
  }

  sensor_t* s = esp_camera_sensor_get();
  if (s) {
    // You can tweak sensor settings here if needed.
    // s->set_brightness(s, 0);
    // s->set_contrast(s, 0);
  }

  Serial.println("[CAM] init OK");
  return true;
}

static bool camera_init_with_retry() {
  const uint8_t max_tries = (CAM_INIT_MAX_RETRIES < 1) ? 1 : CAM_INIT_MAX_RETRIES;
  for (uint8_t i = 0; i < max_tries; i++) {
    Serial.printf("[CAM] init try %u/%u\n", (unsigned)(i + 1), (unsigned)max_tries);

    if (camera_init()) {
      return true;
    }

    // Best-effort cleanup before retry
    esp_camera_deinit();
    delay(CAM_RETRY_DELAY_MS);
  }
  return false;
}

// Convert RGB565 fb buffer to JPEG (software)
static bool fb_to_jpeg(const camera_fb_t* fb, uint8_t jpeg_quality, uint8_t** out_buf, size_t* out_len) {
  *out_buf = nullptr;
  *out_len = 0;
  if (!fb || !fb->buf || fb->len == 0) return false;

  // Encode RGB565 -> JPEG
  bool ok = fmt2jpg(
    fb->buf, fb->len,
    fb->width, fb->height,
    fb->format,
    jpeg_quality,
    out_buf,
    out_len
  );
  return ok;
}

static bool http_upload_frame_with_pred(const camera_fb_t* fb) {
  WiFiClient client;
  client.setTimeout(4000);

  if (!client.connect(COLLECTOR_HOST, COLLECTOR_PORT)) {
    Serial.println("[HTTP] connect failed");
    return false;
  }

  uint8_t* jpg_buf = nullptr;
  size_t jpg_len = 0;
  bool need_free = false;

  if (!fb_to_jpeg(fb, g_jpeg_quality, &jpg_buf, &jpg_len)) {
    Serial.println("[JPEG] encode failed");
    return false;
  }
  need_free = true;

  String boundary = "----gbBoundary";
  boundary += String((uint32_t)esp_random(), HEX);

  auto part_field = [&](const char* name, const String& value) -> String {
    String p;
    p += "--" + boundary + "\r\n";
    p += "Content-Disposition: form-data; name=\"";
    p += name;
    p += "\"\r\n\r\n";
    p += value;
    p += "\r\n";
    return p;
  };

  String head;
  head.reserve(768);
  head += part_field("frame_id", String(g_frame_id));
  head += part_field("device_ts_ms", String((uint32_t)millis()));
  head += part_field("width", String(fb->width));
  head += part_field("height", String(fb->height));
  head += part_field("led_pwm", String(g_led_pwm));

  // Add inference fields (Collector currently ignores unknown fields, but
  // this is useful for future server-side logging without changing firmware).
  if (isfinite(g_prob_raw)) head += part_field("gb_prob", String(g_prob_raw, 6));
  if (isfinite(g_prob_ema)) head += part_field("gb_prob_ema", String(g_prob_ema, 6));
  if (isfinite(g_z)) head += part_field("gb_z", String(g_z, 6));
  head += part_field("gb_use_zscore", String(g_use_zscore ? 1 : 0));
  head += part_field("gb_state", String(g_state_on ? 1 : 0));
  head += part_field("gb_thr_on", String(g_thr_on, 6));
  head += part_field("gb_thr_off", String(g_thr_off, 6));
  head += part_field("gb_z_on", String(g_z_on, 4));
  head += part_field("gb_z_off", String(g_z_off, 4));

  head += "--" + boundary + "\r\n";
  head += "Content-Disposition: form-data; name=\"image\"; filename=\"frame.jpg\"\r\n";
  head += "Content-Type: image/jpeg\r\n\r\n";

  String tail;
  tail.reserve(64);
  tail += "\r\n--" + boundary + "--\r\n";

  const uint32_t content_length = head.length() + jpg_len + tail.length();

  client.print(String("POST ") + COLLECTOR_UPLOAD_PATH + " HTTP/1.1\r\n");
  client.print(String("Host: ") + COLLECTOR_HOST + "\r\n");
  client.print("User-Agent: atoms3r-cam-infer\r\n");
  client.print(String("Content-Type: multipart/form-data; boundary=") + boundary + "\r\n");
  client.print(String("Content-Length: ") + content_length + "\r\n");
  client.print("Connection: close\r\n\r\n");

  client.print(head);
  client.write(jpg_buf, jpg_len);
  client.print(tail);

  // Best-effort response read
  uint32_t t0 = millis();
  while (client.connected() && millis() - t0 < 1000) {
    while (client.available()) {
      (void)client.read();
    }
    break;
  }
  client.stop();

  if (need_free && jpg_buf) free(jpg_buf);
  return true;
}

static bool http_post_event(const char* type, int value, const char* note) {
  WiFiClient client;
  client.setTimeout(3000);

  if (!client.connect(COLLECTOR_HOST, COLLECTOR_PORT)) {
    Serial.println("[HTTP] event connect failed");
    return false;
  }

  StaticJsonDocument<256> doc;
  doc["type"] = type;
  doc["value"] = value;
  doc["note"] = note ? note : "";

  char body[256];
  size_t body_len = serializeJson(doc, body, sizeof(body));

  client.print(String("POST ") + COLLECTOR_EVENT_PATH + " HTTP/1.1\r\n");
  client.print(String("Host: ") + COLLECTOR_HOST + "\r\n");
  client.print("User-Agent: atoms3r-cam-infer\r\n");
  client.print("Content-Type: application/json\r\n");
  client.print(String("Content-Length: ") + String((uint32_t)body_len) + "\r\n");
  client.print("Connection: close\r\n\r\n");
  client.write((const uint8_t*)body, body_len);

  // Best-effort response read
  uint32_t t0 = millis();
  while (client.connected() && millis() - t0 < 800) {
    while (client.available()) {
      (void)client.read();
    }
    break;
  }
  client.stop();
  return true;
}

static bool extract_center_roi_downsample_gray(const camera_fb_t* fb, uint8_t* out_gray) {
  if (!fb || !fb->buf) return false;
  if (fb->format != PIXFORMAT_RGB565) {
    Serial.printf("[INF] unsupported fb format=%d (need RGB565)\n", (int)fb->format);
    return false;
  }

  const int W = (int)fb->width;
  const int H = (int)fb->height;
  const int roi = ROI_SIZE_PX;
  const int S = INPUT_SIZE;

  if (roi <= 0 || S <= 0) return false;
  if (roi > W || roi > H) return false;

  const int x0 = (W - roi) / 2;
  const int y0 = (H - roi) / 2;

  const uint16_t* pix = (const uint16_t*)fb->buf;

  // Nearest-neighbor downsample
  for (int oy = 0; oy < S; oy++) {
    const int sy = y0 + (oy * roi) / S;
    const uint16_t* row = pix + sy * W;
    for (int ox = 0; ox < S; ox++) {
      const int sx = x0 + (ox * roi) / S;
      out_gray[oy * S + ox] = rgb565_to_gray(row[sx]);
    }
  }
  return true;
}

static void lbp_hist_8(const uint8_t* gray, int S, uint32_t* hist) {
  memset(hist, 0, sizeof(uint32_t) * LBP_LR_BINS);

  // 8-neighbor LBP, clockwise bits: 7..0
  for (int y = 1; y < S - 1; y++) {
    const int ym1 = (y - 1) * S;
    const int y0  = y * S;
    const int yp1 = (y + 1) * S;
    for (int x = 1; x < S - 1; x++) {
      const uint8_t c = gray[y0 + x];
      uint8_t code = 0;
      code |= (gray[ym1 + (x - 1)] >= c) ? (1u << 7) : 0;
      code |= (gray[ym1 + (x    )] >= c) ? (1u << 6) : 0;
      code |= (gray[ym1 + (x + 1)] >= c) ? (1u << 5) : 0;
      code |= (gray[y0  + (x + 1)] >= c) ? (1u << 4) : 0;
      code |= (gray[yp1 + (x + 1)] >= c) ? (1u << 3) : 0;
      code |= (gray[yp1 + (x    )] >= c) ? (1u << 2) : 0;
      code |= (gray[yp1 + (x - 1)] >= c) ? (1u << 1) : 0;
      code |= (gray[y0  + (x - 1)] >= c) ? (1u << 0) : 0;
      hist[code]++;
    }
  }
}

static float lbp_lr_predict_prob(const uint32_t* hist, int total_pixels) {
  // dot(w, hist_norm) + b
  // hist_norm[i] = hist[i] / total_pixels
  float acc = LBP_LR_BIAS;
  float sum = 0.0f;
  for (int i = 0; i < LBP_LR_BINS; i++) {
    sum += LBP_LR_WEIGHTS[i] * (float)hist[i];
  }
  acc += sum / (float)total_pixels;
  return sigmoidf_clamped(acc);
}

static void update_prob_ema(float p, float dt_sec) {
  if (!isfinite(g_prob_ema)) {
    g_prob_ema = p;
    return;
  }
  const float tau = (g_prob_tau_sec > 1e-3f) ? g_prob_tau_sec : 0.8f;
  const float a = expf(-dt_sec / tau);
  g_prob_ema = a * g_prob_ema + (1.0f - a) * p;
}

static void reset_zscore_stats(float p_init) {
  g_z_mu = p_init;
  g_z_var = 1e-4f;  // small non-zero
  g_z = 0.0f;
}

static void update_zscore(float p_ema, float dt_sec) {
  // Lightweight z-score based on EMA mean/variance of p_ema.
  // z = (p_ema - mu) / sqrt(var)
  if (!isfinite(p_ema)) {
    g_z = NAN;
    return;
  }
  if (!isfinite(g_z_mu) || !isfinite(g_z_var)) {
    reset_zscore_stats(p_ema);
    return;
  }

  const float tau = (g_z_tau_sec > 1e-3f) ? g_z_tau_sec : ZSCORE_EMA_TAU_SEC;
  const float a = expf(-dt_sec / tau);

  // Use previous mean to compute delta for variance update.
  const float mu_prev = g_z_mu;
  const float delta = p_ema - mu_prev;
  g_z_mu = a * g_z_mu + (1.0f - a) * p_ema;
  g_z_var = a * g_z_var + (1.0f - a) * (delta * delta);

  const float denom = sqrtf(g_z_var);
  if (denom < ZSCORE_EPS) {
    g_z = 0.0f;
  } else {
    g_z = (p_ema - g_z_mu) / denom;
  }
}

static void calib_start() {
  g_calib_active = true;
  g_calib_start_ms = millis();
  g_calib_n = 0;
  g_calib_sum = 0.0;
  g_calib_sum2 = 0.0;
  Serial.println("[CALIB] start");
}

static void calib_done_and_update_threshold() {
  g_calib_active = false;
  const uint32_t dur = millis() - g_calib_start_ms;
  if (dur < CALIB_MIN_MS || g_calib_n < 10) {
    Serial.printf("[CALIB] too short (dur=%ums n=%u). ignored\n", (unsigned)dur, (unsigned)g_calib_n);
    return;
  }

  const double mean = g_calib_sum / (double)g_calib_n;
  const double var = (g_calib_sum2 / (double)g_calib_n) - mean * mean;
  const double std = sqrt(var > 1e-9 ? var : 1e-9);

  float thr_on = (float)(mean + (double)CALIB_K_ON * std);
  float thr_off = (float)(mean + (double)CALIB_K_OFF * std);

  thr_on = clampf(thr_on, 0.01f, 0.99f);
  thr_off = clampf(thr_off, 0.0f, thr_on);

  g_thr_on = thr_on;
  g_thr_off = thr_off;

  Serial.printf("[CALIB] done: mean=%.4f std=%.4f thr_on=%.4f thr_off=%.4f\n", (float)mean, (float)std, g_thr_on, g_thr_off);

  if (ENABLE_EVENT_POST) {
    char note[96];
    snprintf(note, sizeof(note), "mean=%.3f std=%.3f thr_on=%.3f thr_off=%.3f", (float)mean, (float)std, g_thr_on, g_thr_off);
    (void)http_post_event("gb_calib_done", 1, note);
  }
}

static void send_udp_response(const IPAddress& ip, uint16_t port, const JsonDocument& doc) {
  char out[384];
  size_t n = serializeJson(doc, out, sizeof(out));
  Udp.beginPacket(ip, port);
  Udp.write((const uint8_t*)out, n);
  Udp.endPacket();
}

static void handle_udp_cmd() {
  int packetSize = Udp.parsePacket();
  if (packetSize <= 0) return;

  char buf[512];
  int len = Udp.read(buf, sizeof(buf) - 1);
  if (len <= 0) return;
  buf[len] = '\0';

  StaticJsonDocument<256> in;
  DeserializationError err = deserializeJson(in, buf);
  StaticJsonDocument<384> out;

  IPAddress rip = Udp.remoteIP();
  uint16_t rport = Udp.remotePort();

  if (err) {
    out["ok"] = false;
    out["error"] = "json_parse_failed";
    send_udp_response(rip, rport, out);
    return;
  }

  const char* cmd = in["cmd"] | "";
  out["cmd"] = cmd;

  if (strcmp(cmd, "ping") == 0) {
    out["ok"] = true;
    out["device_ts_ms"] = (uint32_t)millis();
    out["ip"] = WiFi.localIP().toString();
    out["upload"] = g_upload_enabled;
    out["led_pwm"] = g_led_pwm;
    out["camera_enabled"] = g_camera_enabled;
    out["infer_enabled"] = g_infer_enabled;
    out["use_zscore"] = g_use_zscore;
    out["gb_state"] = g_state_on ? 1 : 0;
    if (isfinite(g_prob_raw)) out["gb_prob"] = g_prob_raw;
    if (isfinite(g_prob_ema)) out["gb_prob_ema"] = g_prob_ema;
    if (isfinite(g_z)) out["gb_z"] = g_z;
    out["thr_on"] = g_thr_on;
    out["thr_off"] = g_thr_off;
    out["z_on"] = g_z_on;
    out["z_off"] = g_z_off;
    out["calib_active"] = g_calib_active;
  } else if (strcmp(cmd, "start_upload") == 0) {
    g_upload_enabled = true;
    out["ok"] = true;
    out["upload"] = true;
  } else if (strcmp(cmd, "stop_upload") == 0) {
    g_upload_enabled = false;
    out["ok"] = true;
    out["upload"] = false;
  } else if (strcmp(cmd, "set_led") == 0) {
    int pwm = in["pwm"] | -1;
    if (pwm < 0) pwm = 0;
    if (pwm > 255) pwm = 255;
    led_set((uint8_t)pwm);
    out["ok"] = true;
    out["pwm"] = pwm;
  } else if (strcmp(cmd, "set_param") == 0) {
    if (in.containsKey("tau_sec")) {
      g_prob_tau_sec = clampf((float)(in["tau_sec"] | (double)g_prob_tau_sec), 0.05f, 10.0f);
      out["tau_sec"] = g_prob_tau_sec;
    }
    if (in.containsKey("target_fps")) {
      int fps = in["target_fps"] | (int)g_target_fps;
      if (fps < 1) fps = 1;
      if (fps > 30) fps = 30;
      g_target_fps = (uint16_t)fps;
      out["target_fps"] = fps;
    }
    if (in.containsKey("jpeg_quality")) {
      int q = in["jpeg_quality"] | (int)g_jpeg_quality;
      if (q < 2) q = 2;
      if (q > 63) q = 63;
      g_jpeg_quality = (uint8_t)q;
      out["jpeg_quality"] = q;
    }
    if (in.containsKey("thr_on")) {
      g_thr_on = (float)(in["thr_on"] | (double)g_thr_on);
      g_thr_on = clampf(g_thr_on, 0.01f, 0.99f);
      out["thr_on"] = g_thr_on;
    }
    if (in.containsKey("thr_off")) {
      g_thr_off = (float)(in["thr_off"] | (double)g_thr_off);
      g_thr_off = clampf(g_thr_off, 0.0f, g_thr_on);
      out["thr_off"] = g_thr_off;
    }
    if (in.containsKey("use_zscore")) {
      const bool new_use = in["use_zscore"].as<bool>();
      if (new_use != g_use_zscore) {
        g_use_zscore = new_use;
        if (isfinite(g_prob_ema)) reset_zscore_stats(g_prob_ema);
        else { g_z_mu = NAN; g_z_var = NAN; g_z = NAN; }
      }
      out["use_zscore"] = g_use_zscore;
    }
    if (in.containsKey("z_tau_sec")) {
      g_z_tau_sec = clampf((float)(in["z_tau_sec"] | (double)g_z_tau_sec), 1.0f, 300.0f);
      out["z_tau_sec"] = g_z_tau_sec;
    }
    if (in.containsKey("z_on")) {
      g_z_on = clampf((float)(in["z_on"] | (double)g_z_on), -10.0f, 20.0f);
      out["z_on"] = g_z_on;
    }
    if (in.containsKey("z_off")) {
      g_z_off = clampf((float)(in["z_off"] | (double)g_z_off), -10.0f, g_z_on);
      out["z_off"] = g_z_off;
    }
    if (in.containsKey("infer_enabled")) {
      g_infer_enabled = in["infer_enabled"].as<bool>();
      out["infer_enabled"] = g_infer_enabled;
    }
    if (in.containsKey("camera_enabled")) {
      String cam_err;
      const bool en = in["camera_enabled"].as<bool>();
      if (!ensure_camera_enabled(en, &cam_err)) {
        out["ok"] = false;
        out["error"] = cam_err;
        send_udp_response(rip, rport, out);
        return;
      }
      out["camera_enabled"] = g_camera_enabled;
    }
    out["ok"] = true;
  } else if (strcmp(cmd, "calib_start") == 0) {
    calib_start();
    out["ok"] = true;
  } else if (strcmp(cmd, "calib_done") == 0) {
    calib_done_and_update_threshold();
    out["ok"] = true;
  } else if (strcmp(cmd, "reboot") == 0) {
    out["ok"] = true;
    send_udp_response(rip, rport, out);
    delay(100);
    ESP.restart();
    return;
  } else {
    out["ok"] = false;
    out["error"] = "unknown_cmd";
  }

  send_udp_response(rip, rport, out);
}

